#include "../push_swap.h"

static void	check_min2(char *s[], int i)
{
	if (s[i][1] == '2' && s[i][2] == '1' && s[i][3] == '4' && s[i][4] == '7' &&
		s[i][5] == '4' && s[i][6] == '8' && s[i][7] == '3' && s[i][8] == '6' &&
		s[i][9] == '4' && s[i][10] > '8')
		print_error();
}

void	check_min(char *s[], int i)
{
	if (s[i][1] > '2')
		print_error();
	if (s[i][1] == '2' && s[i][2] > '1')
		print_error();
	if (s[i][1] == '2' && s[i][2] == '1' && s[i][3] > '4')
		print_error();
	if (s[i][1] == '2' && s[i][2] == '1' && s[i][3] == '4' && s[i][4] > '7')
		print_error();
	if (s[i][1] == '2' && s[i][2] == '1' && s[i][3] == '4' && s[i][4] == '7' &&
		s[i][5] > '4')
		print_error();
	if (s[i][1] == '2' && s[i][2] == '1' && s[i][3] == '4' && s[i][4] == '7' &&
		s[i][5] == '4' && s[i][6] > '8')
		print_error();
	if (s[i][1] == '2' && s[i][2] == '1' && s[i][3] == '4' && s[i][4] == '7' &&
		s[i][5] == '4' && s[i][6] == '8' && s[i][7] > '3')
		print_error();
	if (s[i][1] == '2' && s[i][2] == '1' && s[i][3] == '4' && s[i][4] == '7' &&
		s[i][5] == '4' && s[i][6] == '8' && s[i][7] == '3' && s[i][8] > '6')
		print_error();
	if (s[i][1] == '2' && s[i][2] == '1' && s[i][3] == '4' && s[i][4] == '7' &&
		s[i][5] == '4' && s[i][6] == '8' && s[i][7] == '3' && s[i][8] == '6' &&
		s[i][9] > '4')
		print_error();
	check_min2(s, i);
}
